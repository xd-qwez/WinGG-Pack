local function getTop10Bitki(BitkiData, Labeles, YourPoints, YourLabels)
    local finalBitki = {}

    for top = 1, 10 do
        local tempBestOrg
        local tempBestOrgPoints = 0

        for org, points in pairs(BitkiData) do

            if not tempBestOrg or points > tempBestOrgPoints then
                tempBestOrg = org
                tempBestOrgPoints = points
            end
        end

        BitkiData[tempBestOrg] = nil
        local lvl = 0

        for i = 1, #ranks do
            if tempBestOrgPoints >= ranks[i] and tempBestOrgPoints <= ranks[i+1] then
                lvl = i
                break
            end
        end

        finalBitki[#finalBitki+1] = {label = Labeles[tempBestOrg], lvl = lvl}
    end

    if YourLabels and YourPoints then
        local lvl = 0
        for i = 1, #ranks do
            if YourPoints > ranks[i] and YourPoints < ranks[i+1] then
                lvl = i
                break
            end
        end
        finalBitki[#finalBitki+1] = {label = YourLabels, lvl = lvl}
    end

    return finalBitki
end

local function getTopStrefy(Strefydata, Labeles)
    local finalStrefy = {}

    for _, data in ipairs(Config['strefy'].Zones) do

        local tempBestOrg
        local tempBestOrgCaptures = 0

        for org, stefy in pairs(Strefydata) do
            for name, captures in pairs(stefy) do
                if data.name == name and captures > tempBestOrgCaptures then
                    tempBestOrg = org
                    tempBestOrgCaptures = captures
                end
            end
        end

        finalStrefy[data.name] = {label = Labeles[tempBestOrg] or "Brak", captures = tostring(tempBestOrgCaptures)}
    end

    return finalStrefy
end

